#ifndef g11_noassert_hpp
#define g11_noassert_hpp

//------------------------------------------------------------------------------
// Assert macros
//------------------------------------------------------------------------------
#define G11_ASSERT_TRUE( cond )     do{} while( 0 )
#define G11_ASSERT_FALSE( cond )    do{} while( 0 )
#define G11_ASSERT_ZERO( cond )     do{} while( 0 )
#define G11_ASSERT_NOT_ZERO( cond ) do{} while( 0 )
#define G11_ASSERT_NULL( cond )     do{} while( 0 )
#define G11_ASSERT_NOT_NULL( cond ) do{} while( 0 )

#endif
